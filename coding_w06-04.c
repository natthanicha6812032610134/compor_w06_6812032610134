#include <stdio.h>

int main() {
    // 1. ลดค่าของ x ลง 4.0
    double x1 = 10.0;
    double x2 = 10.0;

    // แบบปกติ
    x1 = x1 - 4.0;

    // แบบย่อด้วย -=
    x2 -= 4.0;

    printf("1. x1 = %.2f, x2 = %.2f\n", x1, x2); // ทั้งสองจะได้ 6.00

    // 2. คูณค่าของ x ด้วย 6.5
    x1 = 2.0;
    x2 = 2.0;

    // แบบปกติ
    x1 = 6.5 * x1;

    // แบบย่อด้วย *=
    x2 *= 6.5;

    printf("2. x1 = %.2f, x2 = %.2f\n", x1, x2); // ทั้งคู่ได้ 13.00

    // 3. หาค่าเศษจากการหาร โดยใช้การดำเนินการรวมหลายตัวแปร
    int x3 = 20, x4 = 20;
    int y = 2, z = 3, a = 4;

    // แบบปกติ
    x3 = x3 % (y + z * a); // 3*4 = 12 + 2 = 14 → 20 % 14 = 6

    // แบบย่อด้วย %=
    x4 %= (y + z * a);

    printf("3. x3 = %d, x4 = %d\n", x3, x4); // ทั้งสองได้ 6

    // 4. แบ่ง x ด้วย (2.0 * x)
    x1 = 4.0;
    x2 = 4.0;

    // แบบเต็ม
    x1 = x1 / (2.0 * x1); // 4.0 / 8.0 = 0.5

    // แบบย่อด้วย /=
    x2 /= (2.0 * x2);

    printf("4. x1 = %.2f, x2 = %.2f\n", x1, x2); // ทั้งสองได้ 0.50

    // 5. เพิ่มค่าให้ total โดยนำราคาคูณจำนวน แล้วลบส่วนลด
    double price = 10.0, quantity = 2.0, discount = 3.0;
    double total1 = 0, total2 = 0;

    // แบบปกติ
    total1 = total1 + (price * quantity - discount); // 10*2 - 3 = 17

    // แบบย่อด้วย +=
    total2 += (price * quantity - discount);

    printf("5. total1 = %.2f, total2 = %.2f\n", total1, total2); // ทั้งสองได้ 17.00

    // 6. เพิ่มค่า x ตามอัตราร้อยละ (เช่น ดอกเบี้ย 5%)
    double rate = 5.0;
    x1 = 100.0;
    x2 = 100.0;

    // แบบเต็ม
    x1 = x1 * (1 + rate / 100); // 100 * (1 + 0.05) = 105.00

    // แบบย่อด้วย *=
    x2 *= (1 + rate / 100);

    printf("6. x1 = %.2f, x2 = %.2f\n", x1, x2); // ทั้งสองได้ 105.00

    // 7. หักคะแนนตามจำนวนข้อผิดพลาด
    int score1 = 100, score2 = 100;
    int penalty = 2, mistake = 3;

    // แบบปกติ
    score1 = score1 - (penalty * (mistake + 1)); // 2 * (3 + 1) = 8 → 100 - 8 = 92

    // แบบย่อด้วย -=
    score2 -= (penalty * (mistake + 1));

    printf("7. score1 = %d, score2 = %d\n", score1, score2); // ทั้งสองได้ 92

    return 0;
} 