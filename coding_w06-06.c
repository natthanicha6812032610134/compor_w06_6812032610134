#include <stdio.h>

int main() {
    int a = 5, b = 2;
    float x = 3.0, y = 4.5;
    int r1, r2;
    float r3, r4;

    // การคำนวณ r1:
    // a++ หมายถึงใช้ค่า a ก่อน (คือ 5) แล้วค่อยเพิ่มเป็น 6
    // (int)y = 4, 4 % 3 = 1 → r1 = 5 * 2 + 1 = 11
    r1 = a++ * b + (int)y % 3;

    // การคำนวณ r2:
    // ตอนนี้ a เป็น 6, b ยังเป็น 2
    // (a > b) → 6 > 2 → จริง (1)
    // (int)x = 3, 3 / 2 = 1 → 1 < 2 → จริง (1)
    // r2 = 1 && 1 = 1
    r2 = (a > b) && ((int)x / b < 2);

    // การคำนวณ r3:
    // ++x หมายถึงเพิ่มค่า x ก่อนใช้ → x = 4.0
    // 4.0 * 4.5 = 18.0, a = 6 → 6 / 2 = 3 → r3 = 18.0 - 3 = 15.0
    r3 = ++x * y - a / 2;

    // การคำนวณ r4:
    // x += 1.5 → x = 5.5 → 5.5 > y (4.5) → จริง (1)
    // ใช้ || → ถ้าข้างซ้ายเป็นจริง จะไม่ประเมินขวา (short-circuit)
    // ดังนั้น b-- ไม่ทำงาน → b ยังเป็น 2
    // r4 = 1.0
    r4 = ((x += 1.5) > y) || (b-- > 0);

    // แสดงผลลัพธ์ของทุกตัวแปร
    printf("r1 = %d\n", r1);    // 11
    printf("r2 = %d\n", r2);    // 1
    printf("r3 = %.2f\n", r3);  // 15.00
    printf("r4 = %.2f\n", r4);  // 1.00

    return 0;
} 