#include <stdio.h>

int main() {
    int x = 12, y = 7, z = 12;

    // 1. ตรวจสอบว่า x มีค่ามากกว่า y หรือไม่
    printf("1. x > y = %d\n", x > y); 
    // ผลลัพธ์: 1 เพราะ 12 มากกว่า 7

    // 2. ตรวจสอบว่า x มีค่าน้อยกว่า z หรือไม่
    printf("2. x < z = %d\n", x < z); 
    // ผลลัพธ์: 0 เพราะ 12 ไม่ได้น้อยกว่า 12

    // 3. ตรวจสอบว่า x เท่ากับ z หรือไม่
    printf("3. x == z = %d\n", x == z); 
    // ผลลัพธ์: 1 เพราะ x และ z มีค่าเท่ากัน

    // 4. ตรวจสอบว่า x ไม่เท่ากับ y หรือไม่
    printf("4. x != y = %d\n", x != y); 
    // ผลลัพธ์: 1 เพราะ 12 ไม่เท่ากับ 7

    // 5. ตรวจสอบนิพจน์ทางตรรกะที่ซับซ้อน
    printf("5. !(2 * 5 >= y) || (5 != (5 / 3)) = %d\n", !(2 * 5 >= y) || (5 != (5 / 3))); 
    /*
        - 2 * 5 = 10 → 10 >= 7 → จริง (1)
        - !1 → 0
        - 5 / 3 = 1 (หารแบบจำนวนเต็ม) → 5 != 1 → จริง (1)
        - สรุป: 0 || 1 = 1
    */

    // 6. ตรวจสอบว่า x ไม่ได้น้อยกว่า y
    printf("6. !(x < y) = %d\n", !(x < y)); 
    // 12 < 7 = เท็จ (0), !0 = จริง (1)

    // 7. ตรวจสอบว่า ผลรวมของ x กับ y มากกว่าผลคูณของ z กับ 2 หรือไม่
    printf("7. (x + y) > (z * 2) = %d\n", (x + y) > (z * 2)); 
    // (12 + 7) = 19, (12 * 2) = 24 → 19 > 24 = เท็จ (0)

    // 8. ตรวจสอบว่า x เป็นเลขคู่ หรือ y เป็นเลขคี่
    printf("8. (x %% 2 == 0) || (y %% 2 == 1) = %d\n", (x % 2 == 0) || (y % 2 == 1)); 
    // x = 12 เป็นเลขคู่ → จริง, y = 7 เป็นเลขคี่ → จริง → จริง || จริง = จริง (1)

    // 9. ตรวจสอบว่า x มากกว่า y และ z น้อยกว่า y พร้อมกันหรือไม่
    printf("9. (x > y) && (z < y) = %d\n", (x > y) && (z < y)); 
    // x > y = จริง, z < y = เท็จ → จริง && เท็จ = เท็จ (0)

    return 0;
}