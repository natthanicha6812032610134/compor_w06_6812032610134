 #include <stdio.h>

int main() {
    int A, B, C, D, E, F, G;

    // A: เริ่มจากคูณก่อน แล้วจึงบวก → 5 * 2 = 10, แล้ว -2 + 10 = 8
    A = -2 + 5 * 2;
    printf("A = %d\n", A);  // ผลลัพธ์: 8

    // B: แบ่งก่อน แล้วคูณ → 10 / 2 = 5, 5 * 3 = 15
    B = 10 / 2 * 3;
    printf("B = %d\n", B);  // ผลลัพธ์: 15

    // C: เริ่มจากหาค่าเศษ 4 % 2 = 0 → 3 * 0 = 0 → 6 / 2 = 3 → 3 + 0 = 3
    C = 6 / 2 + 3 * (4 % 2);
    printf("C = %d\n", C);  // ผลลัพธ์: 3

    // D: ค่าที่อยู่ในวงเล็บก่อน → (5 + 2) = 7, 7 * 15 = 105 → 105 % 4 = 1
    D = (5 + 2) * 15 % 4;
    printf("D = %d\n", D);  // ผลลัพธ์: 1

    // E: คูณและหารก่อน → 2 * 2 = 4, 6 / 2 = 3 → 6 + 4 - 3 = 7
    E = 6 + 2 * 2 - 6 / 2;
    printf("E = %d\n", E);  // ผลลัพธ์: 7

    // F: เรียงลำดับการคำนวณ:
    // 3 * 2 = 6, 8 / 4 = 2, 6 % 5 = 1 → 5 + 6 - 2 + 1 = 10
    F = 5 + 3 * 2 - 8 / 4 + (6 % 5);
    printf("F = %d\n", F);  // ผลลัพธ์: 10

    // G: วงเล็บก่อน → (4 + 3) = 7, (2 + 3) = 5
    // แล้วคูณและหาร → 7 * 2 = 14, 10 / 5 = 2 → 14 - 2 = 12
    G = (4 + 3) * 2 - 10 / (2 + 3);
    printf("G = %d\n", G);  // ผลลัพธ์: 12

    return 0;
}