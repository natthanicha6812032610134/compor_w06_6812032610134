#include <stdio.h>
#include <math.h>

int main() {
    // 1. กำหนดค่าเริ่มต้นให้กับตัวแปร
    int a = 5, b = 17;
    float c = 8.5, d = 4.0;

    // 2. แสดงค่าของตัวแปรทั้งหมดก่อนเริ่มการคำนวณ
    printf("ค่าเริ่มต้น: a = %d, b = %d, c = %.1f, d = %.1f\n", a, b, c, d);

    // 3. คำนวณผลบวกระหว่าง d กับ a
    printf("d + a = %.1f\n", d + a); // 4.0 + 5 = 9.0

    // 4. คำนวณผลลบของ a ลบด้วย b
    printf("a - b = %d\n", a - b); // 5 - 17 = -12

    // 5. คูณค่าของ c กับ d
    printf("c * d = %.2f\n", c * d); // 8.5 * 4.0 = 34.00

    // 6. นำค่า a คูณกับ c
    printf("a * c = %.2f\n", a * c); // 5 * 8.5 = 42.50

    // 7. หารค่าของ c ด้วย d
    printf("c / d = %.2f\n", c / d); // 8.5 / 4.0 = 2.13

    // 8. คำนวณ b หารด้วย c
    printf("b / c = %.2f\n", b / c); // 17 / 8.5 = 2.00

    // 9. หาค่าที่เหลือจากการหาร a ด้วย b
    printf("a %% b = %d\n", a % b); // 5 % 17 = 5

    // 10. แปลง c เป็นจำนวนเต็มก่อนนำไปหารเอาเศษกับ a
    printf("((int)c) %% a = %d\n", ((int)c) % a); // (int)8.5 = 8 -> 8 % 5 = 3

    // 11. ใช้ฟังก์ชัน fmod เพื่อหารเอาเศษของ c กับ d (แบบทศนิยม)
    printf("fmod(c, d) = %.2f\n", fmod(c, d)); // 8.5 % 4.0 = 0.5

    return 0;
} 