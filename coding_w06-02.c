#include <stdio.h>

int main() {
    // ส่วนของ W06-02-01 (ตัวแปรประเภท int)
    int i = 1, j = 2, k;

    // แสดงค่าตัวแปรเริ่มต้น i = 1 และ j = 2 โดยที่ k ยังไม่ได้กำหนดค่า
    printf("W06-02-01:\n");
    printf("1. i = %d, j = %d, k = uninitialized\n", i, j);

    // นำค่า i และ j มาบวกกัน แล้วเก็บผลลัพธ์ไว้ใน k → 1 + 2 = 3
    k = i + j;
    printf("2. i = %d, j = %d, k = %d\n", i, j, k);

    // อัปเดตค่า i โดยเอา i เดิมมาบวกกับผลคูณของ k กับ 2 → 1 + (3 * 2) = 7
    i = i + (k * 2);
    printf("3. i = %d, j = %d, k = %d\n", i, j, k);

    // แบ่งค่า i ด้วย 2 แล้วนำผลลัพธ์เก็บใน j → 7 / 2 = 3 (หารแบบจำนวนเต็ม)
    j = i / 2;
    printf("4. i = %d, j = %d, k = %d\n", i, j, k);

    // คำนวณเศษจากการหาร i ด้วย 2 แล้วเก็บไว้ใน k → 7 %% 2 = 1
    k = i % 2;
    printf("5. i = %d, j = %d, k = %d\n", i, j, k);

    // นำ j กับ k มาบวกกันก่อน แล้วคูณด้วย 3 → (3 + 1) * 3 = 12
    i = (j + k) * 3;
    printf("6. i = %d, j = %d, k = %d\n\n", i, j, k);

    // ส่วนของ W06-02-02 (ตัวแปรประเภท double)
    double x = 1.0, y = 2.0, z;

    // แสดงค่าตั้งต้นของ x และ y ส่วน z ยังไม่ได้กำหนดค่า
    printf("W06-02-02:\n");
    printf("1. x = %.1f, y = %.1f, z = uninitialized\n", x, y);

    // ปรับค่า x เป็นผลรวมของ y และ 5.0 → 2.0 + 5.0 = 7.0
    x = y + 5.0;
    printf("2. x = %.1f, y = %.1f\n", x, y);

    // นำค่า x มาแบ่งครึ่ง แล้วเก็บผลไว้ใน y → 7.0 / 2.0 = 3.5
    y = x / 2.0;
    printf("3. x = %.1f, y = %.1f\n", x, y);

    // คำนวณ y ใหม่โดยเอา x คูณ 3 แล้วบวกด้วย 4.0 → (7.0 * 3.0) + 4.0 = 25.0
    y = (x * 3.0) + 4.0;
    printf("4. x = %.1f, y = %.1f\n", x, y);

    // ปรับค่า x เป็น -0.5 ลบด้วย y → -0.5 - 25.0 = -25.5
    x = -0.5 - y;
    printf("5. x = %.1f, y = %.1f\n", x, y);

    // นำค่า x กับ y มารวมกันแล้วเก็บใน z → -25.5 + 25.0 = -0.5
    z = x + y;
    printf("6. x = %.1f, y = %.1f, z = %.1f\n", x, y, z);

    return 0;
} 